package com.navyaentertainment.services;

public class BufferDomain {
	private String bufferTime;
	private String gracePeriod;
	/**
	 * @return the bufferTime
	 */
	public String getBufferTime() {
		return bufferTime;
	}
	/**
	 * @param bufferTime the bufferTime to set
	 */
	public void setBufferTime(String bufferTime) {
		this.bufferTime = bufferTime;
	}
	/**
	 * @return the gracePeriod
	 */
	public String getGracePeriod() {
		return gracePeriod;
	}
	/**
	 * @param gracePeriod the gracePeriod to set
	 */
	public void setGracePeriod(String gracePeriod) {
		this.gracePeriod = gracePeriod;
	}
}
